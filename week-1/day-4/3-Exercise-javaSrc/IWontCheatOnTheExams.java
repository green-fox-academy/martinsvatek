public class IWontCheatOnTheExams {

    // Create a program that writes this line 100 times:
    // "I won't cheat on the exam!"

    public static void main(String[] args) {

        for (int a = 1; a < 101; a++) {
            int b = a;
            System.out.println(b + ": I won't cheat on the exam!");
        }
    }
}
